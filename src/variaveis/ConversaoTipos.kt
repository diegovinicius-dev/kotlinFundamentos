package variaveis

fun main() {
    val int1 = 10
    val long1 = int1.toLong()

    println(int1)
    println(long1)

    val d1 = int1.toDouble()

    println(int1)

    val d2 = 25.71
    val i2 = d2.toInt()

    println(d2)
    println(i2)

    val s1 = int1.toString()
    val s2 = "101"
    val i3 = s2.toInt()

    println(s1)
    println(i3)
}